# Static Type: Here the dtype of the varibale has to be defined at the time of declration
# Dynamic Type: Changes at the run based on the latest value
a = 10
print(type(a)) # <class 'int'>
a = 'Ronu'
print(type(a)) # <class 'str'>
# Look at the above code, the type of variable 'a' is changing dynamically
